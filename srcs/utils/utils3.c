#include "minishell.h"

void    close_pipe(t_prompt *p)
{
    	int	i;
    
	i = 1;
	while (i <= p->tokens->nbr_pipe)
	{
		close(p->pipex->fd[i][0]);
		close(p->pipex->fd[i][1]);
		i++;
	}
}